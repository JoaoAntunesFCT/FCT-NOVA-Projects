    /**
     * Updates the jTextNumber with the additional digit.
     * There is the special case of the first digit, or if the text field was zero
     */
    private void handle_digit(String number) {
        if ((state.last_operation() == '=') && first_digit) {
            // If the result from the last addition is not used
            Log("");        // Add empty line to the output
            state.reset();  // Ignore last result
        }
        String str= jTextNumber.getText();      // get the string that was already written
        if (first_digit ||str.equals("0")) {    // Ignore zeros
            jTextNumber.setText(number);        // Set the string  
        } else {
            jTextNumber.setText(str+number);    // Concatenate digit
        }
        first_digit= false; // The next digit is not the first!
    }
    
    /**
     * Updates the operand variable value with the result of the operation.
     * Returns: true if success or false is error
     */
    private boolean calc_number(String number, char operation) {
        try {
            // Converts 'String' to long
            long value= Long.parseLong(number);
            
            if (state.last_operation() != '=') {
                Log(number);                    // Write number if pending operation
            }
            Log(Character.toString(operation));             // Write operation  
            return state.execute_or_push_operation(value, operation);   // Update state
        }
        catch (Exception e) {
            Log("Invalid number: " + e + "\n");
            return false;
        }
    }    
    
